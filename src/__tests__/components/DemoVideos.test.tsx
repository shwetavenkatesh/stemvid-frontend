import { render, screen } from "@testing-library/react";
import DemoVideos from "@/components/landing/DemoVideos";

describe("DemoVideos", () => {
  it("renders all three demo titles", () => {
    render(<DemoVideos />);
    expect(screen.getByText("Attention Is All You Need")).toBeInTheDocument();
    expect(screen.getByText("Dremel: Interactive Analysis")).toBeInTheDocument();
    expect(screen.getByText("MapReduce")).toBeInTheDocument();
  });

  it("shows placeholder text for each video", () => {
    render(<DemoVideos />);
    const placeholders = screen.getAllByText("Video coming soon");
    expect(placeholders).toHaveLength(3);
  });

  it("shows generation time for each demo", () => {
    render(<DemoVideos />);
    const captions = screen.getAllByText("Generated by stemvid.ai in 12 minutes");
    expect(captions).toHaveLength(3);
  });
});
